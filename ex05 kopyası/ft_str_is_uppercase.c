int		ft_str_is_uppercase(char *str)
{
	int		i;
	char	c;
	int a;

	i = 0;
	a = 1;
	while (*(str + i) != '\0')
	{
		c = *(str + i);
		if (c < 65 || c > 90)
		{
			a = 0;
		}
		i++;
	}
	return (a);
}
